#Crie um algoritmo que leia um número e mostre o seu dobro, seu triplo e sua
#raiz quadrada.
n1 = float(input('Digite um número qualquer: '))
print('O dobro desse número é {}.'.format(2*n1))
print('O triplo desse número é {}.'.format(n1*3))
print('A raiz quadrada desse número é {}.'.format(pow(n1,0.5)))
